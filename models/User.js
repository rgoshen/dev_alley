const mongoose = require("mongoose");

// Declare the Schema of the Mongo model
var UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now(),
  },
});

//Export the model
module.exports = mongoose.model("User", UserSchema);
